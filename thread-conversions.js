// Returns match info for a given source thread in the target brand, or null.
// Return value: { id: string, confidence: 'official'|'reconciled'|'single-source' } | null
function getOfficialMatch(sourceBrand, sourceId, targetBrand) {
  var key = sourceBrand + ":" + sourceId;
  var entry = CONVERSIONS[key];
  if (!entry || !entry[targetBrand]) return null;
  return entry[targetBrand]; // { id, confidence }
}
